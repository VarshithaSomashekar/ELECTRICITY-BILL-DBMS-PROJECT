  <div class="centered">Karnataka Power Transmission Corporation Limited (KPTCL) is a state-owned power transmission company in the Indian state of Karnataka. The company was formed in 1999 and is responsible for the transmission of electricity from various power generation sources to the distribution companies in the state.
KPTCL is responsible for the operation, maintenance, and development of the state's transmission network, which includes transmission lines, substations, and other related infrastructure.
KPTCL also has subsidiaries, such as the Bangalore Electricity Supply Company (BESCOM), Hubli Electricity Supply Company (HESCOM), Gulbarga Electricity Supply Company (GESCOM), and Chamundeshwari Electricity Supply Company (CESC), which are responsible for the distribution of electricity to consumers in their respective regions.
